    private void tocartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tocartMouseClicked
        // TODO add your handling code here:
        if (seleziona.getSelectedIndex() != 0) {
            String prod = (String) Prodotti.getSelectedItem();
            int x=(int)Quantita.getValue();
          
                    
            int p = 0;
            int n = 0;
            int i;
            for (i = 0; i < prod.length(); i++) {
                if (prod.charAt(i) == 32 && n == 0) {
                    n = i;
                } else if (prod.charAt(i) == 32 && p == 0) {
                    p = i;
                }
            }
            //  System.out.println("n:"+n+"p:"+p);
            int a=x;
            tot = tot+(( Integer.parseInt(prod.substring(p + 1, prod.length() - 1)))*a);
            prod = prod.substring(0, n);
           
            while(x!=0){
            Cart.addElement(prod);
            lista.setModel(Cart);
            
            x--;
            }
            totale.setText("Totale:" + tot + "‚Ç¨");
        }
    }//GEN-LAST:event_tocartMouseClicked

