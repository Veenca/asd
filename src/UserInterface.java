    private void ConfermaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ConfermaMouseClicked
        // TODO add your handling code here:
        String item, item2;
        int cont = 0;
        for (int i = 0; i < Cart.getSize(); i++) {
            for (int j = i + 1; j < Cart.getSize(); j++) {
                item = (String) Cart.getElementAt(i);
                item2 = (String) (Cart.getElementAt(j));
                if (item.compareTo(item2) > 0) {
                    // test = (String) Cart.getElementAt(j);
                    Cart.setElementAt(item2, i);
                    Cart.setElementAt(item, j);

                }
            }
        }
        
        if (totale.getText().equals("Totale:")) {
            successo.setText("Devi selezionare qualcosa");
            successo.setForeground(Color.red);
        } else {
            System.out.println("Ecco a lei il suo scontrino:\nProdotto-Prezzo:");
            for (int i = 0; i < Cart.getSize(); i++) {
                item = (String) Cart.getElementAt(i);
                cont=0;
                  for (int h = i + 1; h < Cart.getSize(); h++) {
                    if (Cart.getElementAt(h).equals(Cart.getElementAt(h - 1))) {

                        cont++;
                    } else {
                        break;
                    }

                }
                  i = i + cont;
                  
                for (int j = 0; j < Finestra1.i; j++) {
                    if (Finestra1.vet[j].getDescrizione().equals(item)) {
                        if (Finestra1.vet[j].getQuantita() >= 0) {
                            Finestra1.vet[j].setQuantita(Finestra1.vet[j].getQuantita() - (cont+1));

                            System.out.println(Finestra1.vet[j].getDescrizione() + "  x" + (cont + 1) + "    " + Finestra1.vet[j].getPrezzo());
                            break;
                        } else {
                            System.out.println("il prodotto:" + Finestra1.vet[j].getDescrizione() + "non e'disponibile");
                        }
                    }

                }

            }
            Cart.removeAllElements();
            lista.setModel(Cart);
            System.out.println(totale.getText());
            tot = 0;
            totale.setText("Totale:");
            successo.setText("Ordine confermato");
            successo.setForeground(Color.green);

        }
    }//GEN-LAST:event_ConfermaMouseClicked

